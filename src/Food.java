public class Food {
    String name;
    double caloriesper100g;
    double proteinper100g;
    double fatper100g;
    double carbsper100g;

    public Food(String name, double caloriesper100g, double proteinper100g, double fatper100g, double carbsper100g) {
        this.name = name;
        this.caloriesper100g = caloriesper100g;
        this.proteinper100g = proteinper100g;
        this.fatper100g = fatper100g;
        this.carbsper100g = carbsper100g;
    }
}
